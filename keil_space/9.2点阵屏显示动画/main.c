#include <REGX52.H>
#include "Delay.h"
#include "MATRIX.h"

unsigned char annotation[]=	
{
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	0xFF,0x08,0x08,0x08,0xFF,0x00,0x0E,0x15,
	0x15,0x15,0x08,0x00,0x7E,0x01,0x02,0x00,
	0x7E,0x01,0x02,0x00,0x0E,0x11,0x11,0x0E,
	0x00,0x7D,0x00,0x00,0x00,0x00,0x00,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00
};

void main()
{
	unsigned char offset = 0, count = 0 , i;

	while(1)
	{
		for(i=0;i<8;i++)
		{
			MATRIXLED_ShowColumn(i , annotation[i + offset]);
		}
		count++;
		if(count == 5)
		{
			count = 0;
			offset++;
			if(offset == sizeof(annotation) / sizeof(annotation[0]) - 7)
			{
				offset = 0; 
			}
		}
	}
}